using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApplication3
{
    class Circle: Figure
    {
        public Circle (int y, int z)
        {
            this.y = y;
            this.z = z
        }
        
        public double c.Square()
        {
            double c = y*z;
            return c;
        }
    }
}
